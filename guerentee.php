    <h2 align="Center" >Price Accuracy Politicy</h2>

<p class="text-justify">If the charged price of a product is higher than the shelf or advertised price the Customer will receive one of that item free. The Customer will be charged the correct/higher price for any additional items of that product in that order. Items sold by the pound will result in one pound of the item being provided for free and all remaining pounds being sold at the correct/higher price.</p>


<p>Customers must use a bonuscard to receive sale prices for all items excluding government regulated items (Milk, Alcohol, Non-Alcoholic Beverages, Tobacco)</p>
<ul>
<li>Charged price includes prices for items that are scanned or price look-up items (PLU)</li>
<li>Shelf price includes any displayed or posted price. Void when prohibited by Federal, State or Local Law.</li>
<li>Advertised price includes any price within a weekly circular, ROP or targeted mailer.</li>
<li>Exemptions to these prices would be in the form of an ad retraction posted at the Service Center</li>
Exemptions: Government regulated items such as Milk, Alcohol, Non-Alcoholic Beverages, Tobacco Products are exempt from the “free-provision” of the Price Accuracy Guarantee. 
<li>In the event a pricing discrepancy occurs on a government regulated item, the item is to be charged at the lowest represented price to the consumer.</li> 
<li>Items that do not scan as a result of the item not being setup or system issues are excluded from the Price Accuracy Policy.</li>
</ul>

<p class="text-justify">The Price Accuracy Policy is in effect both during and after checkout. During checkout, the Price Accuracy Policy cannot be implemented until the order is totaled because some promotions are not calculated until the end of the order. Whether or not a Customer has paid for their purchase is irrelevant to qualifying for the Price Accuracy Policy. After checkout (whether or not the Customer has left the store), the Price Accuracy Policy is in effect as long as the Customer has a receipt and brings the price error to the store personnel’s attention within 72 hours of purchase. Price Accuracy Policy is only applicable within the week pricing maintenance was applied for the item.</p>

<h2 align="center">Returns</h2>
<p class="text-justify">We stand behind every item we sell. If your purchase did not meet your expectations for any reason, we will refund your purchase price with a smile, and invite you to try a replacement item for free.</p>

<h2 align="center">Rain Checks or Alternate</h2>
<p class="text-justify">Rain Checks or Alternate products are offered to customers when an item the customer is looking to purchase is out of stock due to conditions within our control (excludes product recalls, discontinued items).
</p>


<h4>Rain Checks</h4>
<ul>
<li>Customers seeking a rain check for an out of stock item can request one at the Service Center for the item they are looking to purchase.</li>
<li>
Quantity limit of 10 items on all Rain Checks unless otherwise indicated.</li>
</ul>

<h4>Product Replacements</h4>
<ul>
<li>Customers seeking to receive a replacement product at the same price of the out of stock item should make a request to the department lead, store management team, Checkout Coach or Service Center associate.</li>
<li>
Quantity limit of 10 items on all Product Replacements unless otherwise indicated.</li>
</ul>

